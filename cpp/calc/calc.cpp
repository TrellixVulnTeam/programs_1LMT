double n,v,r;

void addition(double num, double var){
	n = num;
	v = var;
	r = (n + v);
	return r;
}
void substraction(double num, double var){
	n = num;
	v = var;
	r = (n - v);
	return r;
}
void multiplication(double num, double var){
	n = num;
	v = var;
	r = (n * v);
	return r;
}
void division(double num, double var){
	n = num;
	v = var;
	r = (n / v);
	return r;
}

void modulus(double num, double var){
	n = num;
	v = var;
	r = (n % v);
	return r;
 }
 